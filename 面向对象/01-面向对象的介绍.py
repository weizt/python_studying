# 面向过程和面向对象
'''
面向过程：
根据业务逻辑众上到下写代码

面向对象：
将变量和函数绑定到一起，分类进行封装，每个程序只要负责分配给自己的分类，
这样能够更快速的开发程序，减少了重复代码
'''
# 类和对象
'''
类：是指对一群具有相同特征或者行为的事物的统称，是抽象的，不用直接使用
    · 特征其实就是一个变量，在类里我们称之为属性
    · 行为其实就是一个函数，在类里我们称之为方法
    · 类其实就是由属性和方法组成的一个抽象概念

对象：
    对象是由类创建出来的一个具体存在，可以直接使用，由哪一个类创建出来的对象，
    就拥有哪一个类中定义的属性和方法。
'''
