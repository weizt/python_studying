# math模块就是数学相关的模块
import math

# 求阶乘函数
print(math.factorial(6))

# 向下取整
print(math.floor(12.98))
# 向上取整
print(math.ceil(15.001))

# 求幂运算
print(math.pow(2, 10))

# round()内置函数，实现四舍五入到指定位数
x = round(4.55997, 3)
print(x)

# 三角函数
print(math.sin(math.pi / 6))
print(math.cos(math.pi / 3))
print(math.tan(math.pi / 2))
